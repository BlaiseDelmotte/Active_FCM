!!====================================================================
!!
!! CPU remaining time control ... if working
!!
!!====================================================================

subroutine CPUCONTROL(L)

!!=====================================================================
!!
!!
!!=====================================================================

implicit none

!---------------------------------------------------------------------
! ARRAYS STATEMENT
!---------------------------------------------------------------------
!
integer, intent(out) :: L

real :: CPUTIME

!---------------------------------------------------------------------

external tremain
!---------------------------------------------------------------------

!- Call intrinsic subroutine for time
call tremain(cputime)
write (*,*) "temps restant:", CPUTIME

L = 0
if (CPUTIME <= 300) then
 L = 1
 write (*,*) "temps restant:", CPUTIME
end if

end subroutine CPUCONTROL
